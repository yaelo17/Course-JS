/* Operadores de comparación
    > Mayor que
    < Menor que
    >= Mayor o igual que
    <= Menor o igual que
    == Igual que
    === Igual estricto (compara valor y tipo de dato)
    != Diferente que
    !== Diferente estricto (compara valor y tipo de dato)
    ? Operador ternario (condición ? valor1 : valor2)
*/

const a = 10;
const b = 20;
const c = "10";

a == b; // false
a === c; // false
a != b; // true
a !== c; // true
a > b; // false
a < c; // true
a >= b; // false
a <= b; // true